function buildFlashMessage(analyzed, indexSnapshot = []) {
  const { dateStr, weekday, timeStr } = fmtDateHeader();

  const totalCount = analyzed.market.length + analyzed.stocks.length;
  let msg = `<b>вҡЎ зҫҺиӮЎж–°иҒһеҝ«иЁҠ</b>пҪң${dateStr} ${weekday}\n`;
  msg += `${'в”Ғ'.repeat(24)}\n`;

  // в”Җв”Җ жҢҮж•ёеҝ«з…§ в”Җв”Җ
  if (indexSnapshot.length > 0) {
    msg += '\n';
    for (const { name, quote: q } of indexSnapshot) {
      const emoji = q.changePct >= 0 ? 'в–І' : 'в–ј';
      msg += `${trendEmoji(q.changePct)} <b>${name}</b> <code>${fmt(q.price)}</code> ${emoji}<code>${Math.abs(q.changePct).toFixed(2)}%</code>\n`;
    }
  }

  // в”Җв”Җ еӨ§зӣӨдәӢд»¶ в”Җв”Җ
  msg += `\n<b>рҹҢҗ еӨ§зӣӨдәӢд»¶</b>\n`;
  if (analyzed.market.length > 0) {
    for (const item of analyzed.market) {
      const badge = item.importance === 5 ? 'рҹ”ә' : 'в–ё';
      msg += `${badge} <b>${item.summary_zh}</b>`;
      if (item.category) msg += `  <i>${item.category}</i>`;
      msg += '\n';
    }
  } else {
    msg += `  <i>жҳЁж—Ҙз„ЎйҮҚеӨ§зёҪз¶“жҲ–ең°з·ЈдәӢд»¶</i>\n`;
  }

  // в”Җв”Җ еҖӢиӮЎеҝ«иЁҠ в”Җв”Җ
  msg += `\n<b>рҹ“Ң еҖӢиӮЎеҝ«иЁҠ</b>\n`;
  if (analyzed.stocks.length > 0) {
    const sorted = [...analyzed.stocks].sort((a, b) => b.importance - a.importance);
    for (const item of sorted) {
      const badge = item.importance === 5 ? 'рҹ”ә' : 'в–ё';
      msg += `${badge} <b>${item.name}</b>пјҲ<code>${item.symbol}</code>пјү`;
      if (item.category) msg += `  <i>${item.category}</i>`;
      msg += `\n   ${item.summary_zh}\n`;
    }
  } else {
    msg += `  <i>жҳЁж—Ҙжұ е…§еҖӢиӮЎз„ЎйҮҚеӨ§дәӢд»¶</i>\n`;
  }

  msg += `\n${'в”Ғ'.repeat(24)}\n`;
  msg += `<i>вҡЎ ${totalCount} еүҮйҮҚиҰҒж–°иҒһ В· ${timeStr} зҷјеёғ</i>\n`;
  msg += `<i>дҫҶжәҗпјҡFinnhub / Yahoo Finance В· еғ…дҫӣеҸғиҖғ</i>`;
  return msg;
}

