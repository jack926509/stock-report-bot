// зҫҺиӮЎдј‘еёӮж—ҘиЁҲз®—пјҲеҗ«жө®еӢ•еҒҮж—ҘпјүвҖ”вҖ” зөҗжһңжҢүе№ҙеҝ«еҸ–пјҢйҒҝе…ҚйҮҚиӨҮиЁҲз®—
const _holidayCache = new Map();
function getUSMarketHolidays(year) {
  if (_holidayCache.has(year)) return _holidayCache.get(year);
  const holidays = [];
  // еӣәе®ҡеҒҮж—Ҙ
  holidays.push(`${year}-01-01`); // е…ғж—Ұ
  holidays.push(`${year}-06-19`); // е…ӯжңҲзҜҖ Juneteenth
  holidays.push(`${year}-07-04`); // зҚЁз«ӢзҙҖеҝөж—Ҙ
  holidays.push(`${year}-12-25`); // иҒ–иӘ•зҜҖ

  // жө®еӢ•еҒҮж—Ҙпјҡз¬¬ N еҖӢйҖұдёҖ/йҖұеӣӣ
  const nthWeekday = (month, weekday, n) => {
    const first = new Date(year, month - 1, 1);
    let d = ((weekday - first.getDay()) + 7) % 7 + 1;
    d += (n - 1) * 7;
    return new Date(year, month - 1, d);
  };
  const lastWeekday = (month, weekday) => {
    const last = new Date(year, month, 0); // жңҲеә•
    const diff = (last.getDay() - weekday + 7) % 7;
    return new Date(year, month - 1, last.getDate() - diff);
  };

  const mlk        = nthWeekday(1, 1, 3);  // 1жңҲз¬¬3еҖӢйҖұдёҖпјҡMLK Day
  const presidents = nthWeekday(2, 1, 3);  // 2жңҲз¬¬3еҖӢйҖұдёҖпјҡзёҪзөұж—Ҙ
  const memorial   = lastWeekday(5, 1);    // 5жңҲжңҖеҫҢдёҖеҖӢйҖұдёҖпјҡйҷЈдәЎе°ҮеЈ«зҙҖеҝөж—Ҙ
  const labor      = nthWeekday(9, 1, 1);  // 9жңҲз¬¬1еҖӢйҖұдёҖпјҡеӢһеӢ•зҜҖ
  const thanksgiving = nthWeekday(11, 4, 4); // 11жңҲз¬¬4еҖӢйҖұеӣӣпјҡж„ҹжҒ©зҜҖ

  for (const d of [mlk, presidents, memorial, labor, thanksgiving]) {
    holidays.push(d.toISOString().split('T')[0]);
  }

  // иҖ¶з©ҢеҸ—йӣЈж—ҘпјҲеҫ©жҙ»зҜҖеүҚ2еӨ©пјҢйңҖиЁҲз®—пјү
  // дҪҝз”Ё Anonymous Gregorian algorithm
  const a = year % 19, b = Math.floor(year / 100), c = year % 100;
  const d = Math.floor(b / 4), e = b % 4, f = Math.floor((b + 8) / 25);
  const g = Math.floor((b - f + 1) / 3), h = (19 * a + b - d - g + 15) % 30;
  const i = Math.floor(c / 4), k = c % 4;
  const l = (32 + 2 * e + 2 * i - h - k) % 7;
  const m = Math.floor((a + 11 * h + 22 * l) / 451);
  const month = Math.floor((h + l - 7 * m + 114) / 31);
  const day = ((h + l - 7 * m + 114) % 31) + 1;
  const goodFriday = new Date(year, month - 1, day - 2);
  holidays.push(goodFriday.toISOString().split('T')[0]);

  // еӣәе®ҡеҒҮж—ҘиӢҘйҒҮйҖұе…ӯвҶ’йҖұдә”дј‘пјҢйҒҮйҖұж—ҘвҶ’йҖұдёҖдј‘
  const result = holidays.map(dateStr => {
    const dt = new Date(dateStr + 'T12:00:00');
    const dow = dt.getDay();
    if (dow === 6) dt.setDate(dt.getDate() - 1); // йҖұе…ӯ вҶ’ йҖұдә”
    if (dow === 0) dt.setDate(dt.getDate() + 1); // йҖұж—Ҙ вҶ’ йҖұдёҖ
    return dt.toISOString().split('T')[0];
  });
  _holidayCache.set(year, result);
  return result;
}

function isTradingDay() {
  const now  = new Date();
  const day  = now.getDay();
  if (day === 0 || day === 6) { log('STOCK', 'йҖұжң«пјҢи·ійҒҺ'); return false; }
  const todayStr = now.toISOString().split('T')[0];
  const holidays = getUSMarketHolidays(now.getFullYear());
  if (holidays.includes(todayStr)) {
    log('STOCK', `зҫҺиӮЎдј‘еёӮж—ҘпјҲ${todayStr}пјүпјҢи·ійҒҺ`);
    return false;
  }
  return true;
}

